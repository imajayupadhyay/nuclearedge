<script setup>
import { onMounted, onBeforeUnmount, ref, nextTick } from 'vue';
import { Head } from '@inertiajs/vue3';

const rootRef = ref(null);
let hashHandler = null;

onMounted(async () => {
  await nextTick();
  const root = rootRef.value;
  if (!root) return;

  /* ===== Logo fallback chain: primary logo -> Google favicon (by domain) -> clean text-only treatment ===== */
  root.querySelectorAll('.lc img').forEach(function (img) {
    function toText() {
      var cell = img.closest('.lc');
      if (cell) { cell.classList.add('text-only'); img.remove(); }
    }
    img.addEventListener('error', function handler() {
      var d = img.getAttribute('data-domain');
      var isFavicon = img.src.indexOf('google.com/s2/favicons') !== -1;
      if (d && !isFavicon) {
        img.src = 'https://www.google.com/s2/favicons?domain=' + d + '&sz=256';
      } else {
        img.removeEventListener('error', handler);
        toText();
      }
    });
    img.addEventListener('load', function () {
      if (img.src.indexOf('google.com/s2/favicons') !== -1 && img.naturalWidth <= 16) toText();
    });
  });

  /* ===== Sector "View Work" toggles ===== */
  function panelFor(head) {
    var id = head.getAttribute('data-toggle');
    return id ? document.getElementById(id) : null;
  }
  function openHead(head, scroll) {
    var panel = panelFor(head);
    if (!panel) return;
    head.classList.add('open');
    panel.classList.add('open');
    if (scroll) {
      setTimeout(function () {
        head.scrollIntoView({ behavior: 'smooth', block: 'start' });
      }, 60);
    }
  }
  function toggleHead(head) {
    var panel = panelFor(head);
    if (!panel) return;
    var willOpen = !head.classList.contains('open');
    head.classList.toggle('open');
    panel.classList.toggle('open');
    if (willOpen) {
      setTimeout(function () { head.scrollIntoView({ behavior: 'smooth', block: 'start' }); }, 60);
    }
  }

  // click on a sector header toggles its work panel
  root.querySelectorAll('.vert-head.clickable').forEach(function (head) {
    head.addEventListener('click', function () { toggleHead(head); });
  });

  // also let the logo grid of a clickable sector open the panel
  root.querySelectorAll('.vert-head.clickable').forEach(function (head) {
    var grid = head.parentElement.querySelector('.logo-grid');
    if (grid) {
      grid.style.cursor = 'pointer';
      grid.addEventListener('click', function (e) {
        if (e.target.closest('a')) return;
        if (!head.classList.contains('open')) openHead(head, true);
      });
    }
  });

  // If the URL carries a #sector hash, auto-open that sector.
  function openFromHash() {
    var h = location.hash.replace('#', '');
    if (!h) return;
    var sec = document.getElementById(h);
    if (sec) {
      var head = sec.querySelector('.vert-head.clickable');
      if (head && !head.classList.contains('open')) openHead(head, false);
    }
  }
  hashHandler = openFromHash;
  window.addEventListener('hashchange', hashHandler);
  openFromHash();
});

onBeforeUnmount(() => {
  if (hashHandler) window.removeEventListener('hashchange', hashHandler);
});
</script>

